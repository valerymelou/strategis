export function findCountryByDialCode(e164: string): Country | undefined {
  const sorted = [...COUNTRIES].sort(
    (a, b) => b.dialCode.length - a.dialCode.length,
  );
  return sorted.find((c) => e164.startsWith(c.dialCode));
}
