export interface Country {
  iso: string;
  dialCode: string;
  name: string;
}

function flag(iso: string): string {
  const normalizedIso = iso.toUpperCase();

  if (!/^[A-Z]{2}$/.test(normalizedIso)) {
    return '';
  }

  return [...normalizedIso]
    .map((c) => String.fromCodePoint(0x1f1e6 + c.charCodeAt(0) - 65))
    .join('');
}

